# User adds shapes to a garden plan
#
# As a user
# In order to reflect my actual garden
# I want to add shapes to my garden plot
#
# Usage:
#
# User navigates to the existing garden plan
# User drags custom shapes onto the grid
# Shapes are available in circles or rectangles
# Shapes are available in green, brown, and grey
# Shapes can be resized, but their shapes and colors can't be changed after creation
# User clicks Save
# All changes to the user's garden plan are stored to the database
# When the garden is loaded on the edit and home pages, the shapes show up appropriately
